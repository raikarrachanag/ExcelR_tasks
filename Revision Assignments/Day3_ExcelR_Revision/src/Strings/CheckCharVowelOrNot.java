package Strings;

public class CheckCharVowelOrNot {

	public static void main(String[] args) {
		char a='u';
		
		if(a=='a'||a=='e'||a=='i'||a=='o'||a=='u'||a=='A'||a=='E'||a=='I'||a=='O'||a=='U') {
			System.out.println(a + "is a vowel");
		}else {
			System.out.println(a + "is a consonant");
		
		}
	}

}
